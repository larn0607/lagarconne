const getProductByCategory = category =>
  products.filter(product => product.category === category)

const getProductByType = type =>
  products.filter(product => product.type === type)

const getProductByDesigners = designer =>
  products.filter(product => product.designerName === designer)

const getProductByCondition = condition =>
  products.filter(
    product =>
      product.category === condition ||
      product.type === condition ||
      product.designerSlug === condition ||
      product.hasSale === condition
  )

const findProductById = id => products.find(product => product.id === id)

const getProducts = (path, count) => {
  const product = products.filter(
    item =>
      item.category === path ||
      item.type === path ||
      item.designerSlug === path ||
      item.hasSale === path
  )
  let related = null
  if (product.length >= 4) {
    const length = product.length - count
    const start = Math.floor(Math.random() * length)
    related = product.slice(start, start + count)
  } else {
    related = null
  }
  return related
}

const getCartItemsDetail = cartItems => {
  let res = []
  if(cartItems.length > 0) {
    cartItems.forEach(e => {
      res.push({
        ...e,
        product: findProductById(e.productId)
      })
    })
  }
  return res
}

